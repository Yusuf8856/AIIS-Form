            void insertData()
            {
                SimpleDateFormat s1=new SimpleDateFormat("yyyy-MM-dd");
                String dob1=s1.format(dob);
               try
               {
                 Class.forName("com.mysql.cj.jdbc.Driver");
                 String url="jdbc:mysql://localhost:3306/aiis_fms?zeroDateTimeBehavior=CONVERT_TO_NULL";
                 Connection con=DriverManager.getConnection(url,"root","Yusuf@123");
                 
                 String sql="insert into signup values(?,?,?,?,?,?,?)";
                        PreparedStatement st=con.prepareStatement(sql);
                        
                        st.setInt(1, getId());
                        st.setString(2, fname);
                        st.setString(3, lname);
                        st.setString(4, uname);
                        st.setString(5, dob1);
                        st.setString(6, password);
                        st.setString(7, mnumber);
                        int i=st.executeUpdate();
                         if(i>0)
                        {
                            JOptionPane.showMessageDialog(this,"Record Inserted Sucessfully");
                            Login ln=new Login();
                            ln.setVisible(true);
                            this.dispose();
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(this,"Record Not Inserted");
                        }
                      } 
               catch(Exception e)
               {
                   e.printStackTrace();
               }
            }
            
            
            boolean validation()
            {
                fname=txtfname.getText();
                lname=txtlname.getText();
                uname=txtuname.getText();
                password=txtpfield.getText();
                cpassword=txtcpfield1.getText();
                dob=txtdob.getDate();
                mnumber=txtcnumber.getText();
                
                if(fname.equals(""))
                {
                    JOptionPane.showMessageDialog(this,"Please Enter First name...");
                    return false;
                }
                 if(lname.equals(""))
                {
                    JOptionPane.showMessageDialog(this,"Please Enter Last name...");
                    return false;
                }
                  if(uname.equals(""))
                {
                    JOptionPane.showMessageDialog(this,"Please Enter User-Name...");
                    return false;
                }
                   if(dob == null)
                {
                    JOptionPane.showMessageDialog(this,"Please Enter Date Of Birth...");
                    return false;
                }
                   if(password.equals(""))
                {
                    JOptionPane.showMessageDialog(this,"Please Enter Password...");
                    return false;
                }
                    if(cpassword.equals(""))
                {
                    JOptionPane.showMessageDialog(this,"Please Enter Confirm Password...");
                    return false;
                }
                    if(mnumber.equals(""))
                {
                    JOptionPane.showMessageDialog(this,"Please Enter Phone Number...");
                    return false;
                }
                    
                      return true;
            }
